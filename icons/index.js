const mappings = {};
const baseIndex = '9';

availableIcons.forEach((icon, idx) => {
  // Icons are mapped in HEX, so we need to convert the base 10 counter for use.
  let next = idx.toString(16);
  if (next.length < 2) {
    next = `0${next}`;
  }
  mappings[icon] = `\\e${baseIndex}${next}`;
});

module.exports = {
  icons: mappings,
  iconList: availableIcons
};
